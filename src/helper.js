
// 创建script节点
 function createScript(){
	var script = document.createElement("script");
	return script;
}

// 创建link节点
function createStyle(){
	var link = document.createElement("link");
	link.rel = "stylesheet";
    link.type = "text/css";
	return link;
}

// 创建img节点
function createImg(){
	var img = document.createElement("img");
	
	return img;
}